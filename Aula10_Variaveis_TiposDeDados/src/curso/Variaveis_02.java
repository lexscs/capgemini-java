public class Variaveis_02 {
	public static void main(String[] args) {
		//<tipo> <nome da variável>
		//<tipo> <nome da variável> = <valor>
		
		int a;
		a = 5;
		
		int b = 3;
		
		System.out.println(a + b);
		//declara a variável que receberá a soma de a + b
		
		int soma = a + b;
		
		System.out.println(soma);
		
		
		}
}
